export function searchDestinations(query: string): Destination[] {
  if (!query || query.trim().length === 0) {
    return destinations.slice(0, 10);
  }

  const lowerQuery = query.toLowerCase().trim();
  
  return destinations.filter(dest => 
    dest.name.toLowerCase().includes(lowerQuery) ||
    dest.country.toLowerCase().includes(lowerQuery) ||
    dest.continent.toLowerCase().includes(lowerQuery) ||
    dest.description.toLowerCase().includes(lowerQuery) ||
    dest.highlights.some(h => h.toLowerCase().includes(lowerQuery))
  ).slice(0, 10);
}

export function getDestinationById(id: string): Destination | undefined {
  return destinations.find(dest => dest.id === id);
}

export function getDestinationsByCategory(category: string): Destination[] {
  return destinations.filter(dest => 
    dest.categories.includes(category as any)
  );
}

export function getDestinationsByContinent(continent: string): Destination[] {
  return destinations.filter(dest => 
    dest.continent.toLowerCase() === continent.toLowerCase()
  );
}
